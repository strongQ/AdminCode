/**
 * 角色列表输出参数
 * @export
 * @interface RoleOutput
 */
export interface RoleOutput {
    /**
     * Id
     * @type {number}
     * @memberof RoleOutput
     */
    id?: number;
    /**
     * 名称
     * @type {string}
     * @memberof RoleOutput
     */
    name?: string | null;
    /**
     * 编码
     * @type {string}
     * @memberof RoleOutput
     */
    code?: string | null;
}
